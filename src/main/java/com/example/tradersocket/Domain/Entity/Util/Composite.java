package com.example.tradersocket.Domain.Entity.Util;

public  class Composite{
    private int price;
    private int count;

    public Composite(){}

    public int getPrice() {
        return price;
    }

    public void setPrice(int price) {
        this.price = price;
    }

    public int getCount() {
        return count;
    }

    public void setCount(int count) {
        this.count = count;
    }
}
